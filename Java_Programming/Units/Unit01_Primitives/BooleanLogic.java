public class BooleanLogic {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;
            
        boolean boolAnd = a && b;
        System.out.println("Logical AND: " + boolAnd); 
            
     
        boolean boolOr = a || b;
        System.out.println("Logical OR: " + boolOr);   
        
        boolean boolNot = !a;
        System.out.println("Logical NOT of a: " + boolNot);  
        }
    }

