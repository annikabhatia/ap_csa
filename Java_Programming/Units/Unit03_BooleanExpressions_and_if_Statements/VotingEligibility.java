package Units.Unit03_BooleanExpressions_and_if_Statements;

public class VotingEligibility {
    public static void main(String[] args) {
        int age = 19;
        if (age >= 18) {
            System.out.println("This person is eligible to vote in the upcoming election.");
        } else {
            System.out.println("This person is NOT eligible to vote in the upcoming election.");
        }
    }

}
